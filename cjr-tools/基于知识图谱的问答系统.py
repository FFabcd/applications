        "    @property\n",
        "    def _llm_type(self) -> str:\n",
        "        return \"ChatGLM\"\n",
        "\n",
        "    def _call(self,\n",
        "              prompt: str,\n",
        "              stop: Optional[List[str]] = None) -> str:\n",
