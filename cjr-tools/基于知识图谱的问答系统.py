        "            response = enforce_stop_tokens(response, stop)\n",
        "        self.history = self.history + [[None, response]]\n",
        "        return response\n",
        "\n",
        "    def load_model(self,\n",
        "                   model_name_or_path: str = \"THUDM/chatglm-6b\"):\n",
        "        self.tokenizer = AutoTokenizer.from_pretrained(\n",
